package day09_常量变量;

/*
    定义类 Test2
    定义 main方法
    控制台输出5行整数类型常量值
    控制台输出5行小数类型常量值
 */
public class Test02_写 {
    public static void main(String[] args) {
        System.out.println(1);
        System.out.println(2);
        System.out.println(3);
        System.out.println(4);
        System.out.println(5);
        System.out.println(2.1f);
        System.out.println(3.1f);
        System.out.println(4.1f);
        System.out.println(5.1f);
        System.out.println(6.1f);
    }
}
